#!/usr/bin/env node
// One-shot script to create E2E test users via Supabase admin API.
// Usage: node scripts/seed-e2e-users.mjs
// Reads NEXT_PUBLIC_SUPABASE_URL + SUPABASE_SERVICE_ROLE_KEY from .env.local

import { createClient } from '@supabase/supabase-js'
import { readFileSync, appendFileSync, writeFileSync } from 'node:fs'
import { randomBytes } from 'node:crypto'

const envFile = readFileSync('.env.local', 'utf8')
const env = Object.fromEntries(
  envFile.split('\n').filter(l => l.includes('=') && !l.startsWith('#')).map(l => {
    const i = l.indexOf('=')
    return [l.slice(0, i).trim(), l.slice(i + 1).trim()]
  })
)

const url = env.NEXT_PUBLIC_SUPABASE_URL
const serviceRoleKey = env.SUPABASE_SERVICE_ROLE_KEY

if (!url || !serviceRoleKey) {
  console.error('Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in .env.local')
  process.exit(1)
}

const supabase = createClient(url, serviceRoleKey, {
  auth: { autoRefreshToken: false, persistSession: false },
})

function randomPassword() {
  return randomBytes(18).toString('base64').replace(/[+/=]/g, '')
}

async function upsertUser(email, role) {
  // Check if user already exists
  const { data: list } = await supabase.auth.admin.listUsers({ perPage: 200 })
  const existing = list.users.find(u => u.email === email)

  let user, password
  if (existing) {
    password = randomPassword()
    const { data, error } = await supabase.auth.admin.updateUserById(existing.id, {
      password,
      email_confirm: true,
    })
    if (error) throw new Error(`updateUser ${email}: ${error.message}`)
    user = data.user
    console.log(`✓ Updated password for existing user: ${email}`)
  } else {
    password = randomPassword()
    const { data, error } = await supabase.auth.admin.createUser({
      email,
      password,
      email_confirm: true,
      user_metadata: { full_name: email.split('@')[0] },
    })
    if (error) throw new Error(`createUser ${email}: ${error.message}`)
    user = data.user
    console.log(`✓ Created user: ${email}`)
  }

  const { error: roleError } = await supabase
    .from('profiles')
    .update({ role })
    .eq('id', user.id)

  if (roleError) throw new Error(`update profile role for ${email}: ${roleError.message}`)
  console.log(`  role = ${role}`)

  return { email, password }
}

const userCreds = await upsertUser('e2e-user@test.local', 'premium')
const adminCreds = await upsertUser('e2e-admin@test.local', 'admin')

// Write/update .env.local with the creds — remove any prior E2E_* lines first.
const lines = envFile.split('\n')
const withoutE2E = lines.filter(l => !/^E2E_(USER|ADMIN)_(EMAIL|PASSWORD)=/.test(l))
const trailer = withoutE2E[withoutE2E.length - 1] === '' ? '' : '\n'
const newEnv = withoutE2E.join('\n') + trailer + [
  '',
  '# E2E test credentials (generated by scripts/seed-e2e-users.mjs)',
  `E2E_USER_EMAIL=${userCreds.email}`,
  `E2E_USER_PASSWORD=${userCreds.password}`,
  `E2E_ADMIN_EMAIL=${adminCreds.email}`,
  `E2E_ADMIN_PASSWORD=${adminCreds.password}`,
  '',
].join('\n')

writeFileSync('.env.local', newEnv)
console.log('\n✓ Wrote E2E_* vars to .env.local')
console.log('\nReady to run: npm run test:e2e')
